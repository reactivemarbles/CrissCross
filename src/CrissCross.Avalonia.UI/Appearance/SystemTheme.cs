namespace CrissCross.Avalonia.UI.Appearance;

/// <summary>
/// Windows 11 themes.
/// </summary>
public enum SystemTheme
{
    /// <summary>
    /// Unknown Windows theme.
    /// </summary>
    Unknown,

    /// <summary>
    /// Custom Windows theme.
    /// </summary>
    Custom,

    /// <summary>
    /// Default light theme.
    /// </summary>
    Light,

    /// <summary>
    /// Default dark theme.
    /// </summary>
    Dark,

    /// <summary>
    /// High-contrast theme: Desert.
    /// </summary>
    HCWhite,

    /// <summary>
    /// High-contrast theme: Aquatic.
    /// </summary>
    HCBlack,

    /// <summary>
    /// High-contrast theme: Dusk.
    /// </summary>
    HC1,

    /// <summary>
    /// High-contrast theme: Nightsky.
    /// </summary>
    HC2,

    /// <summary>
    /// Dark theme: Glow.
    /// </summary>
    Glow,

    /// <summary>
    /// Dark theme: Captured Motion.
    /// </summary>
    CapturedMotion,

    /// <summary>
    /// Light theme: Sunrise.
    /// </summary>
    Sunrise,

    /// <summary>
    /// Light theme: Flow.
    /// </summary>
    Flow
}
