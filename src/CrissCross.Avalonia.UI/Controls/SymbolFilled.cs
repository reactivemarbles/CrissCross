namespace CrissCross.Avalonia.UI.Controls;

/// <summary>
/// Represents Fluent System Icons filled symbols.
/// </summary>
/// <remarks>
/// This enum contains the same symbols as <see cref="SymbolRegular"/> but uses filled variants.
/// </remarks>
public enum SymbolFilled
{
    /// <summary>
    /// Empty symbol.
    /// </summary>
    Empty = 0,

    /// <summary>
    /// Accept symbol.
    /// </summary>
    Accept = 0xf13e,

    /// <summary>
    /// Account symbol.
    /// </summary>
    Account = 0xf149,

    /// <summary>
    /// Add symbol.
    /// </summary>
    Add = 0xf164,

    /// <summary>
    /// Favorite symbol.
    /// </summary>
    Favorite = 0xf71b,

    /// <summary>
    /// Home symbol.
    /// </summary>
    Home = 0xf80b,

    /// <summary>
    /// Settings symbol.
    /// </summary>
    Settings = 0xfccc,
}
